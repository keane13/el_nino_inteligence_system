function generateEnso() {
  return Object.entries(MONTHLY_ONI).map(([month, oni]) => {
    const [year, mo] = month.split("-");
    const phase = oni >= 0.5 ? "El Niño" : oni <= -0.5 ? "La Niña" : "Netral";
    const abs = Math.abs(oni);
    const strength = abs >= 2.0 ? "Sangat Kuat" : abs >= 1.5 ? "Kuat" : abs >= 1.0 ? "Moderat" : abs >= 0.5 ? "Lemah" : "";
    return {
      month, year: parseInt(year), month_num: parseInt(mo),
      oni_index: oni, phase, strength,
      is_elnino: oni >= 0.5, is_lanina: oni <= -0.5,
    };
  });
}

